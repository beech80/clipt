/**
 * Custom SVG icons implementation
 * These are completely independent from lucide-react to avoid any initialization issues
 */
import React from 'react';

// SVG props interface for all icons
export interface IconProps extends React.SVGProps<SVGSVGElement> {
  size?: number | string;
  strokeWidth?: number;
  absoluteStrokeWidth?: boolean;
}

// Base component for all icons to inherit from
const IconBase = ({ 
  children, 
  size = 24, 
  strokeWidth = 2,
  absoluteStrokeWidth,
  className,
  ...props
}: IconProps & { children: React.ReactNode }) => {
  const computedClassName = className ? `lucide ${className}` : 'lucide';
  const strokeWidthValue = absoluteStrokeWidth
    ? strokeWidth
    : strokeWidth;

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidthValue}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={computedClassName}
      {...props}
    >
      {children}
    </svg>
  );
};

